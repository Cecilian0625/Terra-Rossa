function updateProductInfo(name, price, description, review, calories, sugar, protein) {
  document.getElementById('product-name').innerText = name;
  document.getElementById('product-price').innerText = price;
  document.getElementById('product-description').innerText = description;
  document.getElementById('product-review').innerText = review;
  document.getElementById('product-calories').innerText = calories;
  document.getElementById('product-sugar').innerText = sugar;
  document.getElementById('product-protein').innerText = protein;


  
}